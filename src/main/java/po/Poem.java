package po;

public class Poem {
	private String poemId;
	private String title;
	private String text;
	private String translatedText;
	private String wordsAnnotation;
	private String videoPath;
	private String authorId;
	private String poemComment;
	private String typeId;
	public String getPoemId() {
		return poemId;
	}
	public void setPoemId(String poemId) {
		this.poemId = poemId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getTranslatedText() {
		return translatedText;
	}
	public void setTranslatedText(String translatedText) {
		this.translatedText = translatedText;
	}
	public String getWordsAnnotation() {
		return wordsAnnotation;
	}
	public void setWordsAnnotation(String wordsAnnotation) {
		this.wordsAnnotation = wordsAnnotation;
	}
	public String getVideoPath() {
		return videoPath;
	}
	public void setVideoPath(String videoPath) {
		this.videoPath = videoPath;
	}
	public String getAuthorId() {
		return authorId;
	}
	public void setAuthorId(String authorId) {
		this.authorId = authorId;
	}
	public String getPoemComment() {
		return poemComment;
	}
	public void setPoemComment(String poemComment) {
		this.poemComment = poemComment;
	}
	public String getTypeId() {
		return typeId;
	}
	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}
}
