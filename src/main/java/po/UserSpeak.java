package po;

public class UserSpeak {
   private long userSpeakId;
   private long keyId;
   private int  userId;
   private String speakText;
public long getUserSpeakId() {
	return userSpeakId;
}
public void setUserSpeakId(long userSpeakId) {
	this.userSpeakId = userSpeakId;
}
public long getKeyId() {
	return keyId;
}
public void setKeyId(long keyId) {
	this.keyId = keyId;
}
public int getUserId() {
	return userId;
}
public void setUserId(int userId) {
	this.userId = userId;
}
public String getSpeakText() {
	return speakText;
}
public void setSpeakText(String speakText) {
	this.speakText = speakText;
}
}
